package cleancode.integerdivision;

public class DividendAndIndex {
	
	private int dividend;
	private int index;
		
	public DividendAndIndex(int dividend, int index) {
		this.dividend = dividend;
		this.index = index;
	}
	
	public int getDividend() {
		return dividend;
	}
	public void setDividend(int dividend) {
		this.dividend = dividend;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	
	
}
